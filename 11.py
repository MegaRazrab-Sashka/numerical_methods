import numpy as np

# Параметры задачи
a, b = 0.9, 1.2
h = 0.1
n = int((b - a) / h)  # n = 3
x = np.linspace(a, b, n + 1)  # [0.9, 1.0, 1.1, 1.2]

# Подготовка трёхдиагональной системы
N = n + 1  # 4 узла
A = np.zeros(N)  # нижняя диагональ (коэффициент при y_{i-1})
B = np.zeros(N)  # главная диагональ (коэффициент при y_i)
C = np.zeros(N)  # верхняя диагональ (коэффициент при y_{i+1})
D = np.zeros(N)  # правая часть

# Заполнение для внутренних узлов i = 1, 2 (x = 1.0, 1.1)
for i in range(1, N - 1):
    # y'' - x*y' + 2y = x+1
    # Аппроксимация: (y_{i-1}-2y_i+y_{i+1})/h^2 - x_i*(y_{i+1}-y_{i-1})/(2h) + 2*y_i = x_i+1
    # Умножаем на h^2 и группируем:
    left = 1.0 - (x[i] * h) / 2.0  # коэффициент при y_{i-1}
    center = -2.0 + 2.0 * h * h  # коэффициент при y_i (так как q=2)
    right = 1.0 + (x[i] * h) / 2.0  # коэффициент при y_{i+1}
    rhs = h * h * (x[i] + 1.0)

    A[i] = left
    B[i] = center
    C[i] = right
    D[i] = rhs

# Левое краевое условие: y(0.9) - 0.5*y'(0.9) = 2
# Аппроксимация производной: y'(0.9) ≈ (-3y0 + 4y1 - y2)/(2h)
# Подставляем: y0 - 0.5*(-3y0+4y1-y2)/(2h) = 2
# При h=0.1: y0 - 0.5*(-3y0+4y1-y2)/0.2 = 2 → y0 - 2.5*(-3y0+4y1-y2) = 2
# y0 + 7.5y0 - 10y1 + 2.5y2 = 2 → 8.5y0 - 10y1 + 2.5y2 = 2
# Умножаем на 2: 17y0 - 20y1 + 5y2 = 4
A[0] = 0.0
B[0] = 17.0
C[0] = -20.0
D[0] = 4.0 - 5.0 * 0.0  # временно, y2 пока неизвестен, позже скорректируем

# Правое краевое условие: y(1.2) = 1
A[N - 1] = 0.0
B[N - 1] = 1.0
C[N - 1] = 0.0
D[N - 1] = 1.0

# Так как левое условие содержит y2, нужно привести систему к трёхдиагональному виду
# Выразим y2 из первого внутреннего уравнения (i=1) и подставим в левое условие
# Уравнение для i=1: A[1]*y0 + B[1]*y1 + C[1]*y2 = D[1] → y2 = (D[1] - A[1]*y0 - B[1]*y1)/C[1]
# Подставляем в левое условие: 17y0 - 20y1 + 5*((D[1] - A[1]*y0 - B[1]*y1)/C[1]) = 4
# Умножаем на C[1] и решаем

# Коэффициенты для i=1 (x=1.0)
h_val = h
x1 = x[1]  # 1.0
A1 = 1.0 - (x1 * h_val) / 2.0
B1 = -2.0 + 2.0 * h_val * h_val
C1 = 1.0 + (x1 * h_val) / 2.0
D1 = h_val * h_val * (x1 + 1.0)

# Левое условие после подстановки:
# 17*y0 - 20*y1 + 5*( (D1 - A1*y0 - B1*y1)/C1 ) = 4
# Умножаем на C1:
# 17*C1*y0 - 20*C1*y1 + 5*D1 - 5*A1*y0 - 5*B1*y1 = 4*C1
# Группируем:
# (17*C1 - 5*A1)*y0 + (-20*C1 - 5*B1)*y1 = 4*C1 - 5*D1

C1_coef = C1
coef_y0 = 17.0 * C1_coef - 5.0 * A1
coef_y1 = -20.0 * C1_coef - 5.0 * B1
rhs_left = 4.0 * C1_coef - 5.0 * D1

# Теперь система для y0, y1, y2, y3 (y3=1)
# Уравнение 1 (модифицированное левое): coef_y0*y0 + coef_y1*y1 = rhs_left
# Уравнение 2 (i=2, x=1.1): A2*y1 + B2*y2 + C2*y3 = D2
# Уравнение 3 (правое): y3 = 1

# Коэффициенты для i=2 (x=1.1)
x2 = x[2]  # 1.1
A2 = 1.0 - (x2 * h_val) / 2.0
B2 = -2.0 + 2.0 * h_val * h_val
C2 = 1.0 + (x2 * h_val) / 2.0
D2 = h_val * h_val * (x2 + 1.0)

# Решаем систему 3x3 (y0, y1, y2)
M = np.array([
    [coef_y0, coef_y1, 0.0],
    [0.0, A2, B2],
    [0.0, 0.0, 1.0]
])
R = np.array([rhs_left, D2 - C2 * 1.0, 1.0])

sol = np.linalg.solve(M, R)
y0, y1, y2 = sol
y3 = 1.0

# Вывод результатов
print("Результат решения краевой задачи методом прогонки:")
print(f"y({x[0]:.1f}) = {y0:.8f}")
print(f"y({x[1]:.1f}) = {y1:.8f}")
print(f"y({x[2]:.1f}) = {y2:.8f}")
print(f"y({x[3]:.1f}) = {y3:.8f}")

# Проверка левого краевого условия
y_prime_0 = (-3 * y0 + 4 * y1 - y2) / (2 * h)
left_res = y0 - 0.5 * y_prime_0
print(f"\nПроверка левого условия: y(0.9) - 0.5*y'(0.9) = {left_res:.8f} (должно быть 2)")

# Проверка правого условия
print(f"Проверка правого условия: y(1.2) = {y3:.8f} (должно быть 1)")