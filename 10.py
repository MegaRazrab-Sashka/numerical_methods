# Построение графиков
plt.figure(figsize=(10,5))
plt.plot(x_rk, y_rk, 'o-', label='Рунге-Кутта 4-го порядка (h=0.1)')
plt.plot(x_ad, y_ad, 's-', label='Адамс (предиктор-корректор)')
plt.xlabel('x')
plt.ylabel('y')
plt.title('Решение задачи Коши')
plt.legend()
plt.grid(True)
