import numpy as np

# Исходные данные
x = np.array([0.115, 0.120, 0.125, 0.130, 0.135, 0.140])
y = np.array([8.65729, 8.29329, 7.95829, 7.64893, 7.36235, 7.09613])

n = len(x) - 1          # количество отрезков
h = np.diff(x)          # шаги h_i = x[i+1] - x[i]

# Правая часть системы для определения вторых производных (метод прогонки)
# Естественные граничные условия: c0 = 0, c_n = 0, где c_i = S''(x_i)/2
# Для i = 1..n-1 (в нумерации с 0):
# h[i-1]*c[i-1] + 2*(h[i-1]+h[i])*c[i] + h[i]*c[i+1] = 3*( (y[i+1]-y[i])/h[i] - (y[i]-y[i-1])/h[i-1] )

# Подготовка диагоналей и правой части
A = np.zeros((n+1, n+1))   # матрица для c (размер n+1)
b = np.zeros(n+1)

# Граничные условия: c0 = 0, cn = 0
A[0, 0] = 1
A[n, n] = 1

# Заполнение внутренних уравнений
for i in range(1, n):
    A[i, i-1] = h[i-1]
    A[i, i] = 2*(h[i-1] + h[i])
    A[i, i+1] = h[i]
    b[i] = 3 * ( (y[i+1] - y[i])/h[i] - (y[i] - y[i-1])/h[i-1] )

# Решение системы
c = np.linalg.solve(A, b)   # c_i = S''(x_i)/2

# Вычисление коэффициентов a_i, b_i, d_i для каждого отрезка
a = y[:-1]                  # a_i = y_i
b_coef = np.zeros(n)
d = np.zeros(n)

for i in range(n):
    b_coef[i] = (y[i+1] - y[i])/h[i] - h[i]*(2*c[i] + c[i+1])/3
    d[i] = (c[i+1] - c[i])/(3*h[i])

print("Коэффициенты кубического сплайна (естественные граничные условия S''(x1)=0, S''(x6)=0):\n")
for i in range(n):
    print(f"Отрезок {i+1}: x ∈ [{x[i]:.3f}, {x[i+1]:.3f}]")
    print(f"S_{i+1}(x) = {a[i]:.8f} + {b_coef[i]:.8f}*(x-{x[i]:.3f}) + {c[i]:.8f}*(x-{x[i]:.3f})^2 + {d[i]:.8f}*(x-{x[i]:.3f})^3\n")

# Проверка в узлах
print("Проверка значений в узлах:")
for i in range(n+1):
    if i == 0:
        val = a[0]  # на левом конце
    elif i == n:
        # правый конец последнего отрезка
        val = a[n-1] + b_coef[n-1]*h[n-1] + c[n-1]*h[n-1]**2 + d[n-1]*h[n-1]**3
    else:
        # внутренний узел - совпадает с началом следующего отрезка
        val = a[i-1] + b_coef[i-1]*h[i-1] + c[i-1]*h[i-1]**2 + d[i-1]*h[i-1]**3
    print(f"x = {x[i]:.3f}, y = {y[i]:.8f}, сплайн = {val:.8f}, разность = {val - y[i]:.2e}")

# Проверка непрерывности первой производной во внутренних узлах
print("\nПроверка непрерывности первой производной во внутренних узлах:")
for i in range(1, n):
    # производная слева: S_{i-1}'(x_i) = b_coef[i-1] + 2*c[i-1]*h[i-1] + 3*d[i-1]*h[i-1]^2
    left_der = b_coef[i-1] + 2*c[i-1]*h[i-1] + 3*d[i-1]*h[i-1]**2
    # производная справа: S_i'(x_i) = b_coef[i] (так как x - x_i = 0)
    right_der = b_coef[i]
    print(f"x = {x[i]:.3f}, левая = {left_der:.8f}, правая = {right_der:.8f}, разность = {left_der - right_der:.2e}")

# Проверка непрерывности второй производной во внутренних узлах
print("\nПроверка непрерывности второй производной во внутренних узлах:")
for i in range(1, n):
    left_sec = 2*c[i-1] + 6*d[i-1]*h[i-1]   # S'' слева
    right_sec = 2*c[i]                       # S'' справа
    print(f"x = {x[i]:.3f}, левая = {left_sec:.8f}, правая = {right_sec:.8f}, разность = {left_sec - right_sec:.2e}")

# Вывод сплайнов в виде полиномов от x (без смещения)
print("\nСплайны в развернутом виде (от x):")
for i in range(n):
    # S_i(x) = a_i + b_i*(x-x_i) + c_i*(x-x_i)^2 + d_i*(x-x_i)^3
    # Раскрываем скобки: S_i(x) = (a_i - b_i*x_i + c_i*x_i^2 - d_i*x_i^3) + (b_i - 2c_i*x_i + 3d_i*x_i^2)*x + (c_i - 3d_i*x_i)*x^2 + d_i*x^3
    xi = x[i]
    coef0 = a[i] - b_coef[i]*xi + c[i]*xi**2 - d[i]*xi**3
    coef1 = b_coef[i] - 2*c[i]*xi + 3*d[i]*xi**2
    coef2 = c[i] - 3*d[i]*xi
    coef3 = d[i]
    print(f"S_{i+1}(x) = {coef0:.8f} + {coef1:.8f}*x + {coef2:.8f}*x^2 + {coef3:.8f}*x^3,  x ∈ [{x[i]:.3f}, {x[i+1]:.3f}]")